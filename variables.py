# Variable "integer" (Entero)
numero = 147
print(numero)
print(type(numero))

numero = 147.5
print(numero)
print(type(numero))

complejo = 1 + 2j
print(complejo)
print(type(complejo))

palabra = "Hola"
print(palabra)
print(type(palabra))

bandera = True
print(bandera)
print(type(bandera))

vacio = None
print(vacio)
print(type(vacio))
